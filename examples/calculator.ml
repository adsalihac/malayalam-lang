ഫങ്ക്ഷൻ കൂട്ടുക(a, b) {
  മടങ്ങി a + b
}

ഫങ്ക്ഷൻ കുറയ്ക്കുക(a, b) {
  മടങ്ങി a - b
}

ഫങ്ക്ഷൻ ഗുണിക്കുക(a, b) {
  മടങ്ങി a * b
}

ഫങ്ക്ഷൻ ഭാഗിക്കുക(a, b) {
  മടങ്ങി a / b
}

കാണിക്കുക("കൂട്ടുക: 5 + 3 = ")
കാണിക്കുക(കൂട്ടുക(5, 3))

കാണിക്കുക("കുറയ്ക്കുക: 10 - 4 = ")
കാണിക്കുക(കുറയ്ക്കുക(10, 4))

കാണിക്കുക("ഗുണിക്കുക: 6 * 7 = ")
കാണിക്കുക(ഗുണിക്കുക(6, 7))

കാണിക്കുക("ഭാഗിക്കുക: 20 / 4 = ")
കാണിക്കുക(ഭാഗിക്കുക(20, 4))
