പേര് = "നാരായണൻ"
പ്രായം = 25
ഗ്രേഡ് = "എ"

കാണിക്കുക("പേര്: ")
കാണിക്കുക(പേര്)

കാണിക്കുക("പ്രായം: ")
കാണിക്കുക(പ്രായം)

കാണിക്കുക("ഗ്രേഡ്: ")
കാണിക്കുക(ഗ്രേഡ്)

// Arithmetic
